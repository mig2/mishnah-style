#!/usr/bin/env python3
"""Render the entities knowledge base into a static HTML site (display phase 2).

Usage:
    python3 scripts/kb-render.py
    python3 scripts/kb-render.py --data entities/data --out entities/site

Pure views on the KB (docs/entities-display.md §4–5): one page per entity, plus
aggregate views — a who's-who index (people), a map (places), and a flora gallery
(plants). These never contain mishna text; appearances are deep links into the
canonical masechot via the anchor scheme in docs/html-format.md.

Reads the YAML source of truth (full fidelity); knowledge.db remains the
cross-entity query index per KB spec §11. Output is derived & gitignored.

Requires: pyyaml.
"""

import argparse
import html
import sys
from pathlib import Path

try:
    import yaml
except ImportError:
    sys.exit("kb-render: PyYAML is required — pip install pyyaml")

